export type Click = {
    x:number,
    y:number,
}

/*
Este código exporta un tipo de dato "Click" que representa un objeto con dos propiedades "x" e "y". 
Estas propiedades representan las coordenadas en el eje x e y de un punto de click en una interfaz de usuario, 
como un canvas o una imagen. Este tipo de dato se utiliza para almacenar la información de un click y 
pasarlo como argumento a funciones que trabajan con clicks.
 */